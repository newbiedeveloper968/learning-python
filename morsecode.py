def morse_to_standard(code):
    words = code.split(" / ")
    decoded = []
    for word in words:
        letters = word.split(" ")
        code_to_letter = [morse_code_dict.get(letter, "") for letter in letters]
        decoded.append("".join(code_to_letter))
    return " ".join(decoded)

user_input = input('Morse code: ')
print(morse_to_standard(user_input))